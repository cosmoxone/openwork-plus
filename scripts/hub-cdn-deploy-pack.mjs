#!/usr/bin/env node
/**
 * S2-C11：生成 Hub CDN 运维上传包（zip + catalog + manifest + checklist）。
 * 前置：pnpm run bundle-hub:preflight 或 bundle-hub:build + publish-remote-catalog。
 */
import assert from "node:assert/strict";
import { createHash } from "node:crypto";
import { cp, mkdir, readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(here, "..");
const zipDir = path.join(root, "dist", "bundle-hub");
const outDir = path.join(zipDir, "deploy-pack");

async function main() {
  const manifest = JSON.parse(
    await readFile(path.join(root, "bundles", "knowledge-mgmt", "bundle.json"), "utf8"),
  );
  const version = manifest.version;
  const zipName = `knowledge-mgmt-${version}.zip`;
  const zipPath = path.join(zipDir, zipName);
  assert.ok(existsSync(zipPath), `missing ${zipPath} — run pnpm run bundle-hub:build first`);

  const catalogRemote = path.join(zipDir, "catalog.remote.json");
  const catalogLocal = path.join(zipDir, "catalog.json");
  const catalogSrc = existsSync(catalogRemote) ? catalogRemote : catalogLocal;
  assert.ok(existsSync(catalogSrc), "missing catalog.remote.json or catalog.json");

  const zipBytes = await readFile(zipPath);
  const sha256 = createHash("sha256").update(zipBytes).digest("hex");
  const catalog = JSON.parse(await readFile(catalogSrc, "utf8"));
  const entry = catalog.bundles?.find((b) => b.id === "knowledge-mgmt");
  assert.ok(entry, "catalog missing knowledge-mgmt");

  await mkdir(outDir, { recursive: true });
  await cp(zipPath, path.join(outDir, zipName));
  await cp(catalogSrc, path.join(outDir, "catalog.json"));

  const manifestDoc = {
    generatedAt: new Date().toISOString(),
    bundleId: "knowledge-mgmt",
    version,
    zipName,
    zipBytes: zipBytes.length,
    sha256,
    catalogDownloadUrlHint: "Deploy catalog.json to your fixed Catalog URL (e.g. https://hub.example.com/catalog.json)",
    zipCdnBaseHint: "Upload zip to CDN prefix referenced by catalog downloadUrl",
    files: [
      { name: zipName, sha256, bytes: zipBytes.length, upload: "CDN zip prefix" },
      { name: "catalog.json", upload: "Fixed Catalog URL path", contentType: "application/json" },
    ],
    checklist: [
      "Upload zip to CDN; verify GET downloadUrl",
      "Deploy catalog.json to fixed HTTPS Catalog URL",
      "Set Settings › Bundles › Catalog URL or OPENWORK_BUNDLE_CATALOG_URL",
      "Desktop: install knowledge-mgmt from remote catalog → uninstall",
      "See docs/17-bundle-hub-d-release-plan.md §8.4 C–G",
    ],
  };

  await writeFile(path.join(outDir, "deploy-manifest.json"), JSON.stringify(manifestDoc, null, 2), "utf8");
  await writeFile(
    path.join(outDir, "UPLOAD.md"),
    [
      `# Hub CDN deploy pack — knowledge-mgmt@${version}`,
      "",
      "## Files",
      "",
      `- \`${zipName}\` (${zipBytes.length} bytes, sha256 \`${sha256}\`)`,
      "- `catalog.json` → deploy as **fixed Catalog URL**",
      "",
      "## Ops steps",
      "",
      ...manifestDoc.checklist.map((s) => `- [ ] ${s}`),
      "",
      "Generated by `pnpm run bundle-hub:deploy-pack`.",
    ].join("\n"),
    "utf8",
  );

  console.log(`PASS: deploy pack → ${outDir}`);
  console.log(`  ${zipName} + catalog.json + deploy-manifest.json + UPLOAD.md`);
}

main().catch((e) => {
  console.error("FAIL:", e instanceof Error ? e.message : e);
  process.exitCode = 1;
});
